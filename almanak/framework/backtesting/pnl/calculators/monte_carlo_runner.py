"""Monte Carlo simulation runner for backtesting.

This module provides functionality to run backtests across multiple simulated
price paths generated by the MonteCarloPathGenerator. It enables statistical
analysis of strategy performance across many possible market scenarios.

Key Components:
    - MonteCarloConfig: Configuration for Monte Carlo simulation
    - MonteCarloPathResult: Result from a single path backtest
    - MonteCarloSimulationResult: Aggregated results from all paths
    - SimulatedPricePathProvider: Data provider using simulated paths
    - run_monte_carlo: Main function to run Monte Carlo backtests

Example:
    from almanak.framework.backtesting.pnl.calculators.monte_carlo import (
        MonteCarloPathGenerator,
    )
    from almanak.framework.backtesting.pnl.calculators.monte_carlo_runner import (
        MonteCarloConfig,
        run_monte_carlo,
    )

    # Generate price paths
    generator = MonteCarloPathGenerator()
    paths = generator.generate_price_paths(historical_prices, n_paths=1000)

    # Configure and run simulation
    mc_config = MonteCarloConfig(n_paths=1000)
    result = await run_monte_carlo(strategy, paths, backtest_config, mc_config)

    # Analyze results
    print(f"95% CI: [{result.return_percentile_5th}, {result.return_percentile_95th}]")
"""

